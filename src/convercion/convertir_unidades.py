def convertir_ah_a_wh(ah, voltaje):
    """
    Convierte la capacidad de una batería de Ah a Wh.

    Fórmula:
        Wh = Ah × V

    Donde:
        Wh : energía almacenada (Wh)
        Ah : capacidad de la batería (Ah)
        V  : voltaje de la batería (V)

    Parámetros:
        ah (float): capacidad en amperios-hora.
        voltaje (float): voltaje de la batería.

    Retorna:
        float: energía en Wh.
"""

